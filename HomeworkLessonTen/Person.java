package HomeworkLessonTen;

public class Person {

    String firstName;
    String lastName;
    String dateOfBirth;
    String placeOfBirth;
    String address;


    public Person(String firstName, String lastName, String dateOfBirth, String placeOfBirth, String address) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.dateOfBirth = dateOfBirth;
        this.placeOfBirth = placeOfBirth;
        this.address = address;

    }
}


