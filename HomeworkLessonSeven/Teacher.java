package HomeworkLessonSeven;

public class Teacher
{

    String name;
    String surname;
    int age;

    public  void setName (String n)
    {
     this.name = n;
    }

    public  void setSurname (String s)
    {
        this.surname = s;
    }


    public  void setAge (int a)
    {
        this.age = a;
    }


    public String getName()
    {
        return name;
    }

    public String getSurname()
    {
        return surname;
    }

    public int getAge()
    {
        return age;
    }

}
